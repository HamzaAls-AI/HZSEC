// ─── Crash reporting (Sentry) ────────────────────────────────────────────────
//
// MUST be imported first so it can wrap unhandled errors before any of our
// code runs. The DSN is read from env / a baked config; if it's empty,
// @sentry/electron quietly no-ops — safe to ship without configuring.
try {
  const Sentry = require('@sentry/electron/main');
  const dsn = process.env.SENTRY_DSN || '';
  if (dsn) {
    Sentry.init({
      dsn,
      release: `hzsec@${require('./package.json').version}`,
      // Ship only error+ events; sampling can be tuned later via env.
      tracesSampleRate: parseFloat(process.env.SENTRY_TRACES_RATE || '0'),
      // Strip filesystem paths from stack traces — they can contain user
      // home dirs which we'd rather not centralise.
      beforeSend(event) {
        if (event.exception?.values) {
          for (const v of event.exception.values) {
            v.stacktrace?.frames?.forEach(f => {
              if (f.filename) f.filename = f.filename.replace(/^.*[\\/]/, '');
            });
          }
        }
        return event;
      }
    });
  }
} catch (err) {
  // Sentry itself failing should never crash the app.
  console.warn('[sentry] init skipped:', err.message);
}

