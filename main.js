// ─── Fixes ────────────────────────────────────────────────────────────────────

ipcMain.handle('apply-safe-fix', async (_, issue) => {
  try {
    if (!issue?.filePath || !issue?.lineNumber) throw new Error('Issue missing file path or line number.');

    const filePath = path.resolve(issue.filePath);
    const content = fs.readFileSync(filePath, 'utf8');
    const lines = content.split(/\r?\n/);
    const index = issue.lineNumber - 1;

    if (index < 0 || index >= lines.length) throw new Error('Line number out of range.');

    const originalLine = lines[index];
    const replacement = buildSafeReplacement(originalLine, issue);

    if (!replacement || replacement === originalLine) throw new Error('No safe automatic fix available.');

    const backupPath = createBackup(filePath);
    lines[index] = replacement;
    fs.writeFileSync(filePath, lines.join('\n'), 'utf8');

    appendAuditLog({
      action: 'fix-applied',
      target: filePath,
      detail: `Line ${issue.lineNumber} · ${issue.title} · fixType: ${issue.fixType}`,
      before: originalLine.trim(),
      after: replacement.trim()
    });

    return { success: true, filePath, backupPath, originalLine, newLine: replacement };
  } catch (error) {
    return { success: false, error: error.message };
  }
});

// Agentic fix — apply a batch of line replacements from the assistant's fix plan
ipcMain.handle('apply-agent-fixes', async (_, fixes) => {
  const results = [];

  for (const fix of fixes) {
    try {
      const filePath = path.resolve(fix.filePath);
      const content = fs.readFileSync(filePath, 'utf8');
      const lines = content.split(/\r?\n/);
      const index = fix.lineNumber - 1;

      if (index < 0 || index >= lines.length) {
        results.push({ ...fix, success: false, error: 'Line out of range.' });
        continue;
      }

      const backupPath = createBackup(filePath);
      lines[index] = fix.newLine;
      fs.writeFileSync(filePath, lines.join('\n'), 'utf8');

      results.push({ ...fix, success: true, backupPath });
    } catch (err) {
      results.push({ ...fix, success: false, error: err.message });
    }
  }

  const applied = results.filter(r => r.success);
  const failed  = results.filter(r => !r.success);

  if (applied.length > 0) {
    appendAuditLog({
      action: 'agent-fix-applied',
      detail: `${applied.length} fix(es) applied, ${failed.length} failed`,
      files: [...new Set(applied.map(r => path.basename(r.filePath || '')))].join(', ')
    });
  }

  return { success: true, results };
});

