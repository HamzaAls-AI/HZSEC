ipcMain.handle('start-monitor', async (_, targetPath) => {
  try {
    if (!targetPath) throw new Error('No target selected for monitoring.');

    function scanSingleFile(filePath) {
      return scanFile(filePath, MODE_DEFINITIONS.full, []);
    }

    function onEvent(payload) {
      sendToRenderer('monitor-event', payload);

      if (payload.type === 'ai-alert' && payload.aiMessage) {
        fireNotification(`⚠ ShieldOps: ${payload.filename}`, payload.aiMessage);
      } else if (payload.type === 'new-issues' && !currentApiKey) {
        const top = payload.findings[0];
        fireNotification(
          `⚠ ShieldOps: ${payload.findings.length} new issue(s) in ${payload.filename}`,
          top ? `${top.severity}: ${top.title}` : 'Open ShieldOps for details.'
        );
      }
    }

    monitorState = createSmartMonitor(targetPath, scanSingleFile, onEvent, getApiKey, monitorState);

    appendAuditLog({
      action: 'monitor-started',
      target: targetPath,
      detail: 'Live monitor activated'
    });

    return { success: true, targetPath: monitorState.targetPath };
  } catch (error) {
    return { success: false, error: error.message };
  }
});

ipcMain.handle('stop-monitor', async () => {
  stopLiveMonitor(monitorState);
  monitorState = { watcher: null, targetPath: null, debounceTimers: {} };
  appendAuditLog({ action: 'monitor-stopped', detail: 'Live monitor deactivated' });
  return { success: true };
});

