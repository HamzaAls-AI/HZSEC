// ─── License (HZSec managed-key flow) ────────────────────────────────────────

async function validateLicenseKey(key) {
  return new Promise((resolve) => {
    const url = new URL(BACKEND_URL + '/api/license/validate');
    const lib = url.protocol === 'http:' ? require('http') : require('https');
    const body = JSON.stringify({ licenseKey: key });
    const req = lib.request({
      hostname: url.hostname,
      port:     url.port || (url.protocol === 'http:' ? 80 : 443),
      path:     url.pathname,
      method:   'POST',
      headers:  { 'Content-Type': 'application/json', 'Content-Length': Buffer.byteLength(body) }
    }, res => {
      let data = '';
      res.on('data', c => { data += c; });
      res.on('end', () => {
        try {
          const parsed = JSON.parse(data);
          if (res.statusCode >= 200 && res.statusCode < 300 && parsed.valid) {
            saveLicenseValidation(parsed);
            resolve({ ok: true, ...parsed });
          } else {
            resolve({ ok: false, status: res.statusCode, ...parsed });
          }
        } catch (e) {
          resolve({ ok: false, error: 'parse_error: ' + e.message });
        }
      });
    });
    req.on('error', err => resolve({ ok: false, error: err.message }));
    req.setTimeout(8000, () => { req.destroy(); resolve({ ok: false, error: 'timeout' }); });
    req.write(body);
    req.end();
  });
}

ipcMain.handle('set-license', async (_, key) => {
  const trimmed = (key || '').trim().toUpperCase();
  if (!trimmed) return { success: false, error: 'license key is empty' };
  if (!/^HZSEC(-[A-Z2-9]{4}){4}$/.test(trimmed)) {
    return { success: false, error: 'invalid format — expected HZSEC-XXXX-XXXX-XXXX-XXXX' };
  }
  const validation = await validateLicenseKey(trimmed);
  if (!validation.ok) {
    return { success: false, error: validation.error || 'license rejected by backend', detail: validation };
  }
  setLicense(trimmed);
  currentLicenseKey = trimmed;
  appendAuditLog({ action: 'license-set', detail: 'License key saved + validated' });
  return { success: true, validation };
});

