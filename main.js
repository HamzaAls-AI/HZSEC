// ─── Auto-updates (electron-updater) ─────────────────────────────────────────
//
// The strategy:
//   1. On launch (after window is created) ask GitHub Releases for the latest
//      published version. autoUpdater knows where to look from the `publish`
//      block in package.json's "build" config.
//   2. If a newer version exists, autoUpdater downloads it in the background
//      and fires `update-downloaded`. We forward that to the renderer so it
//      can pop the "Update available" modal.
//   3. The renderer calls back via 'updater:install' when the user accepts —
//      we then call quitAndInstall() which restarts on the new version.
//
// In dev (electron .) the updater no-ops because there's no app-update.yml;
// that file is only generated by electron-builder during a real build.

let autoUpdater = null;
function setupAutoUpdater() {
  if (process.env.ELECTRON_DEV === '1' || !app.isPackaged) {
    console.log('[updater] skipped (not packaged)');
    return;
  }
  try {
    // Lazy-load so an unpackaged dev run doesn't have to install the dep.
    autoUpdater = require('electron-updater').autoUpdater;
  } catch (err) {
    console.warn('[updater] electron-updater not installed:', err.message);
    return;
  }

  autoUpdater.autoDownload = true;
  autoUpdater.autoInstallOnAppQuit = true;

  autoUpdater.on('checking-for-update',  ()    => sendToRenderer('updater:status', { state: 'checking' }));
  autoUpdater.on('update-available',     (info) => sendToRenderer('updater:status', { state: 'available',  version: info?.version }));
  autoUpdater.on('update-not-available', ()    => sendToRenderer('updater:status', { state: 'current' }));
  autoUpdater.on('error',                (err) => {
    console.warn('[updater] error:', err.message);
    sendToRenderer('updater:status', { state: 'error', message: err.message });
  });
  autoUpdater.on('download-progress', (p) => {
    sendToRenderer('updater:progress', {
      percent:    Math.round(p.percent),
      bytesPerSec: p.bytesPerSecond,
      transferred: p.transferred,
      total:       p.total
    });
  });
  autoUpdater.on('update-downloaded', (info) => {
    sendToRenderer('updater:downloaded', {
      version:    info?.version,
      releaseDate: info?.releaseDate,
      releaseNotes: info?.releaseNotes
    });
  });

  // Fire the first check ~5s after launch — gives the renderer a moment to
  // mount + register the IPC listener before any download progress arrives.
  setTimeout(() => {
    autoUpdater.checkForUpdatesAndNotify().catch(err =>
      console.warn('[updater] initial check failed:', err.message)
    );
  }, 5_000);
}

ipcMain.handle('updater:check-now', async () => {
  if (!autoUpdater) return { ok: false, error: 'updater_not_initialized' };
  try {
    const r = await autoUpdater.checkForUpdates();
    return { ok: true, version: r?.updateInfo?.version || null };
  } catch (err) {
    return { ok: false, error: err.message };
  }
});

ipcMain.handle('updater:install', async () => {
  if (!autoUpdater) return { ok: false, error: 'updater_not_initialized' };
  // quitAndInstall(isSilent, isForceRunAfter) — second arg ensures the new
  // version launches automatically after the swap.
  setImmediate(() => autoUpdater.quitAndInstall(false, true));
  return { ok: true };
});

// Register the custom URL scheme so users can sign in via the website and
// have the license key handed back to the desktop app via `hzsec://license/...`.
// Single-instance lock ensures a deep-link click while the app is already
// running activates the existing window instead of spawning a duplicate.
if (process.defaultApp) {
  if (process.argv.length >= 2) {
    app.setAsDefaultProtocolClient('hzsec', process.execPath, [path.resolve(process.argv[1])]);
  }
} else {
  app.setAsDefaultProtocolClient('hzsec');
}

const gotLock = app.requestSingleInstanceLock();
if (!gotLock) {
  app.quit();
} else {
  app.on('second-instance', (_event, argv) => {
    if (mainWindow) {
      if (mainWindow.isMinimized()) mainWindow.restore();
      mainWindow.focus();
    }
    // Windows / Linux deep links arrive in argv on second instance.
    const url = argv.find(a => typeof a === 'string' && a.startsWith('hzsec://'));
    if (url) handleDeepLink(url);
  });
}

// macOS deep links arrive via 'open-url'.
app.on('open-url', (event, url) => {
  event.preventDefault();
  handleDeepLink(url);
});

function handleDeepLink(rawUrl) {
  try {
    const u = new URL(rawUrl);
    if (u.protocol !== 'hzsec:') return;
    // hzsec://license/HZSEC-XXXX-...   (path = "/license/HZSEC-...")
    // hzsec://license?key=HZSEC-...    (also accept query-string variant)
    if (u.hostname === 'license' || u.pathname.startsWith('/license')) {
      const fromPath  = (u.pathname.replace(/^\/?license\/?/, '') || '').trim();
      const fromQuery = u.searchParams.get('key') || u.searchParams.get('licenseKey') || '';
      const key = (fromPath || fromQuery).toUpperCase();
      if (!key) return;
      setLicense(key);
      currentLicenseKey = key;
      // Validate in the background — don't block the deep-link return.
      validateLicenseKey(key).catch(() => {});
      sendToRenderer('license-updated', { licenseKey: key });
    }
  } catch (err) {
    console.warn('[deep-link] could not parse', rawUrl, err.message);
  }
}

